const ProfileSideBar = ({setPage}) => {
  const [isActive, setIsActive] = useState(0);
  const navigate = useNavigate()
  return (
    <div className={styles["side_bar"]}>
      <ul>
        {links.map((link, i) => (
          <li
            key={i}
            onClick={() => {
              setIsActive(i);
              setPage(link.path)
              if (link.path == "/") {
                localStorage.clear()
                sessionStorage.clear()
                navigate(link.path)
              }
            }}
            style={{
              borderBottom: i == 1 || i == 3 ? "1px solid #11111140" : "",
              paddingBottom: i == 1 || i == 3 ? "10px" : "",
            }}
          >
            <div className="icon">{link.icon}</div>
            <p
              style={{
                color: link.color
                  ? link.color
                  : isActive == i
                  ? "#111111"
                  : isActive !== i && "#11111180",
              }}
            >
              {link.title}
            </p>
          </li>
        ))}
      </ul>
    </div>
  );
};

